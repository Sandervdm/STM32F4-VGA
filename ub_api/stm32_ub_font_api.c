struct blinkSkeleton blinkData[10];
unsigned char blinkDataCounter = 0;

void font_blink_poll(void)
{
	if(blinkDataCounter > 0) {
		unsigned char i;
		for(i=0;i<blinkDataCounter;i++) {
			if(blinkData[i].State == 1) {
				font_write_string(blinkData[i].str, blinkData[i].font_type, STYLE_NONE, blinkData[i].posX, blinkData[i].posY, blinkData[i].size, BACKGROUND_COLOR);
				blinkData[i].State = 0;
			}
			else {
				font_write_string(blinkData[i].str, blinkData[i].font_type, STYLE_NONE, blinkData[i].posX, blinkData[i].posY, blinkData[i].size, blinkData[i].color);
				blinkData[i].State = 1;
			}
		}
	}
}

void font_write_string(char* str, FONT_TYPE font_type, STYLE_TYPE style_type, unsigned int posX, unsigned int posY, unsigned char size, unsigned char color)
{
	//create counters
	unsigned char i = 0;
	unsigned char j = 0;
	unsigned int OffsetCounter = 0;
	unsigned short** font_ptr;

	//copy data to array
	if(style_type == STYLE_FLASH) {
		blinkData[blinkDataCounter].State = 1;
		blinkData[blinkDataCounter].color = color;
		blinkData[blinkDataCounter].font_type = font_type;
		blinkData[blinkDataCounter].posX = posX;
		blinkData[blinkDataCounter].posY = posY;
		blinkData[blinkDataCounter].size = size;
		unsigned char i = 0;
		while(*str != 0) {
			blinkData[blinkDataCounter].str[i] = *str;
			str++;
			i++;
			if(i==50)
				break;
		}
		blinkDataCounter++;
	}

	switch (font_type) {
		case FONT_ONE:
			font_ptr = GammaRay;
		break;
		case FONT_TWO:
			font_ptr = AngryBirds;
		break;
		case FONT_THREE:
			font_ptr = Kristen_ITC;
		break;
	}


	//size limitations
	if(size < 10) {
		size = 10;
	}
	else if(size > 40) {
		size = 40;
	}

	//size calculations
	float StepSizeFloat = (FONT_CHAR_HEIGHT * 100) / size;
	StepSizeFloat /= 100;
	int widthVal = FONT_CHAR_WIDTH / StepSizeFloat;

	//print the data
	while(*str != 0) {
		//sanity check character
		unsigned char Char_Pos = 62; // if unknown char, place question mark
		if(*str > 31 && *str < 128) {
			Char_Pos = *str - 32;
		}

		//write character data to ram
		for(j = 0; j < size; j++) {
			for(i = 0; i < widthVal; i++) {
				unsigned char offset_arr_x = i * StepSizeFloat;
				unsigned char offset_arr_y = j * StepSizeFloat;

				if(font_ptr[Char_Pos][offset_arr_x] & (1<<offset_arr_y)) {
					UB_VGA_SetPixel(posX + OffsetCounter + i, posY + j, color);
				}
			}
		}

		//increment counters
		OffsetCounter += widthVal + 1;
		str++;
	}
}
